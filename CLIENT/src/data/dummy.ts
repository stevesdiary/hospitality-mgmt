// ─── Convenience helpers ──────────────────────────────────────────────────────

/** Returns all rooms that belong to a hotel */
export const getRoomsByHotel = (hotelId: string): Room[] =>
  DUMMY_ROOMS.filter((r) => r.hotelId === hotelId);

/** Returns all reservations for a user */
export const getReservationsByUser = (userId: string): Reservation[] =>
  DUMMY_RESERVATIONS.filter((r) => r.userId === userId);

/** Returns all reviews for a hotel */
export const getReviewsByHotel = (hotelId: string): RatingAndReview[] =>
  DUMMY_REVIEWS.filter((r) => r.hotelId === hotelId);

/** Returns a single hotel by id */
export const getHotelById = (id: string): Hotel | undefined =>
  DUMMY_HOTELS.find((h) => h.id === id);

/** Returns a single room by id */
export const getRoomById = (id: string): Room | undefined =>
  DUMMY_ROOMS.find((r) => r.id === id);

/** Returns hotels filtered by city (case-insensitive) */
export const searchHotelsByCity = (city: string): Hotel[] =>
  DUMMY_HOTELS.filter((h) => h.city.toLowerCase().includes(city.toLowerCase()));
